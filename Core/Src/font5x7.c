bool Font5x7_IsCharacterSupported(char c)
{
    return c >= FONT_FIRST_CHAR && c <= FONT_LAST_CHAR;
}

const uint8_t *Font5x7_GetGlyph(char c)
{
    if (!Font5x7_IsCharacterSupported(c))
        c = '?';

    return font5x7[c - FONT_FIRST_CHAR];
}
